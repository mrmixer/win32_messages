uint32_t g_win32_message_count = sizeof( g_win32_messages ) / sizeof( g_win32_messages[ 0 ] );

win32_message_t g_user_message = { win32_message_ls( "Unknown - \"Private Window class\" range - WM_USER (0x0400) -> 0x7FFF" ), 0xffffffff };
win32_message_t g_app_message = { win32_message_ls( "Unknown - \"Application Private\" range - WM_APP (0x8000) -> 0xBFFF" ), 0xffffffff };
win32_message_t g_string_message = { win32_message_ls( "Unknown - \"Registered messages\" range - 0xC000 -> 0xFFFF" ), 0xffffffff };
win32_message_t g_system_message = { win32_message_ls( "Unknown - \"System reserved\" range - 0x10000 -> 0xFFFFFFFF" ), 0xffffffff };

#define g_win32_message_registerd "Registered message: "
#define g_win32_message_buffer_length 1024
char g_win32_message_buffer[ g_win32_message_buffer_length ] = g_win32_message_registerd;
win32_message_t g_registered_message = { g_win32_message_buffer, 0, 0xffffffff };

#undef win32_message_ls

win32_message_t* win32_message_get_first_with_value( uint32_t value ) {
    
    win32_message_t* result = 0;
    
    uint32_t start = 0;
    uint32_t end = g_win32_message_count - 1;
    
    while ( start <= end ) {
        
        uint32_t middle = start + ( end - start ) / 2;
        
        if ( g_win32_messages[ middle ].value == value ) {
            
            while ( middle > 0 && g_win32_messages[ middle - 1 ].value == value ) {
                middle--;
            }
            
            result = g_win32_messages + middle;
            break;
        }
        
        if ( value < g_win32_messages[ middle ].value ) {
            end = middle - 1;
        } else {
            start = middle + 1;
        }
    }
    
    if ( !result ) {
        
        if ( value >= 0x0400 && value < 0x8000 ) {
            result = &g_user_message;
        } else if ( value >= 0x8000 && value < 0xC000 ) {
            result = &g_app_message;
        } else if ( value >= 0xC000 && value <= 0xFFFF ) {
            
            u32 offset = sizeof( g_win32_message_registerd ) - 1;
            uint32_t length = GetClipboardFormatNameA( value, g_win32_message_buffer + offset, g_win32_message_buffer_length - offset );
            
            if ( length ) {
                g_registered_message.label = g_win32_message_buffer;
                g_registered_message.length = offset + length;
                g_registered_message.value = value;
                result = &g_registered_message;
            } else {
                result = &g_string_message;
            }
            
        } else if ( value > 0xFFFF ) {
            result = &g_system_message;
        }
    }
    
    return result;
}

uint32_t win32_message_get_next_with_value( win32_message_t** message ) {
    
    uint32_t result = 0;
    
    if ( message && ( *message ) > g_win32_messages && ( *message ) < g_win32_messages + ( g_win32_message_count - 1 ) ) {
        
        uint32_t value = ( *message )->value;
        ( *message )++;
        
        if ( ( *message )->value == value ) {
            result = 1;
        } else {
            ( *message ) = 0;
        }
        
    } else {
        
        ( *message ) = 0;
    }
    
    return result;
}

win32_message_t* win32_message_get_from_string( char* string, uint32_t length ) {
    
    win32_message_t* result = 0;
    
    for ( uint32_t i = 0; i < g_win32_message_count; i++ ) {
        
        if ( g_win32_messages[ i ].length == length ) {
            
            char* a = string;
            char* b = g_win32_messages[ i ].label;
            
            uint32_t found = 1;
            
            for ( uint32_t c_index = 0; c_index < length; c_index++ ) {
                if ( a[ c_index ] != b[ c_index ] ) {
                    found = 0;
                    break;
                }
            }
            
            if ( found ) {
                result = g_win32_messages + i;
                break;
            }
        }
    }
    
    return result;
}

